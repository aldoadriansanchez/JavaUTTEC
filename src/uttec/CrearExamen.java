    private void NumerKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NumerKeyTyped
        // TODO add your handling code here:
        char a = evt.getKeyChar();
        if (a < '0' || a > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_NumerKeyTyped
    public void materI() {

        try {

            int idpr = conec.IdProfesor(Profe.getText());
            ResultSet rs = conec.enviaMater(idpr);
            while (rs.next()) {
                mater.addItem(rs.getString("nombre"));
            }

        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

