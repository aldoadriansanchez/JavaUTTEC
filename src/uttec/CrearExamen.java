    private void btnguareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguareActionPerformed
        if (!Numer.getText().equals("")){
                if (!Codigo.getText().equals("")) {

            try {
//               String materia = (String) mater.getSelectedItem();
                String ho = (String) hor.getSelectedItem();
                String min = (String) Minu.getSelectedItem();
                String se = (String) sec.getSelectedItem();

                String now = ho + ":" + min + ":" + se;
int seguro= JOptionPane.showConfirmDialog(rootPane, "Seguro que desea guardar el tiempo "+now
+"  que se tardara el examen");
 System.out.println(seguro);
            if(seguro==0){
                int idp = conec.IdProfesor(Profe.getText());
                int idm = conec.consultarMateria((String) mater.getSelectedItem());
                conec.creIns(idm, idp, Integer.parseInt(Numer.getText()), now, Integer.parseInt(Codigo.getText()));
            }else{
             JOptionPane.showMessageDialog(rootPane, "Error");
            }
            
            } catch (SQLException ex) {
                Logger.getLogger(CrearExamen.class.getName()).log(Level.SEVERE, null, ex);
            }
                }else {
            JOptionPane.showMessageDialog(rootPane, "le falta seleccionar las preguntas requeridas\n"
                    + "precione el boton preguntas para accesar");
        } 
        }else {
                 JOptionPane.showMessageDialog(rootPane, "le falta agregar datos");
        }

    }//GEN-LAST:event_btnguareActionPerformed

